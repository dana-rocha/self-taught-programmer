# Research Python sets. When would you use a set?



# Sets are used to store multiple items in a single variable.
# A set is a collection that is unordered, unindexed, and do not allow for duplicate values.
# Sets are written with curly brackets.

# Sets are also unchangeable but you can add new items.
# Since sets do not allow for duplicate values, they could be used for things that don't change but you want
# to add on to like a set of cell phone numbers or social security numbers.
